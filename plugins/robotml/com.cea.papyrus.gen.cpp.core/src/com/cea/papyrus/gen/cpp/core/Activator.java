package com.cea.papyrus.gen.cpp.core;

import org.eclipse.jface.preference.IPreferenceStore;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

import com.cea.papyrus.gen.cpp.core.preferences.CodegenPreferencePage;

/**
 * The activator class controls the plug-in life cycle
 */
public class Activator extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "com.cea.papyrus.gen.cpp.core";

	// The shared instance
	private static Activator plugin;

	/**
	 * Returns the shared instance
	 * 
	 * @return the shared instance
	 */
	public static Activator getDefault() {
		return plugin;
	}

	/**
	 * The constructor
	 */
	public Activator() {
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#initializeDefaultPreferences(org.eclipse.jface.preference.IPreferenceStore)
	 */
	@Override
	protected void initializeDefaultPreferences(IPreferenceStore store) {
		store.setDefault(CodegenPreferencePage.P_HEADER_SUFFIX, "h");
		store.setDefault(CodegenPreferencePage.P_IMPLEM_SUFFIX, "cpp");

		// Default value for P_COMMENT_HEADER
		String defaultValue = "";
		String NL = System.getProperties().getProperty("line.separator");
		defaultValue = defaultValue
				+ "/************************************************************"
				+ NL;
		defaultValue = defaultValue + " *" + NL;
		defaultValue = defaultValue + " * Code Generated by Papyrus C++" + NL;
		defaultValue = defaultValue + " *" + NL;
		defaultValue = defaultValue + " * CEA-List" + NL;
		defaultValue = defaultValue + " *" + NL;
		defaultValue = defaultValue
				+ " ***********************************************************/"
				+ NL;

		store.setDefault(CodegenPreferencePage.P_COMMENT_HEADER, defaultValue);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	@Override
	public void start(BundleContext context) throws Exception {
		super.start(context);
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	@Override
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

}
